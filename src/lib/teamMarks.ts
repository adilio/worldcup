export function teamInitials(team: string): string {
  return team
    .split(/\s+/)
    .filter(Boolean)
    .slice(0, 2)
    .map((part) => part[0])
    .join("")
    .toUpperCase();
}

export function teamMark(team: string): { text: string; flag: boolean } {
  const flag = TEAM_FLAGS[team];
  return flag ? { text: flag, flag: true } : { text: teamInitials(team), flag: false };
}
